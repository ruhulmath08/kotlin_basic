fun main(args: Array<String>) {
    /*
    The difference between var and val is that variables declared with the var
    keyword can be changed/modified, while val variables cannot.
    */

    var name = "John"
    var address: String
    val birthyear = 1975

    address = "Naogaon";

    println(name)          // Print the value of name
    println(birthyear)
    println(address);
}